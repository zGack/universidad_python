# Profundizando Sistemas Numericos

# Decimal
a = 10
# Binario
a = 0b1010
# Octal
a = 0o12
# Hexadecimal
a = 0xA

# Convertir un tipo entero, incluyendo base
# Base decimal
a = int('23', 10)
# Base binaria
a = int('10111', 2)
# Base octal
a = int('27', 8)
# Base hexadecimal
a = int('17', 16)

print(f'a: {a}')

